package model;

public class PriceModel
{
    public String rankfrom;
    public String rankto;
    public String rankamount;

    public String getRankfrom() {
        return rankfrom;
    }

    public void setRankfrom(String rankfrom) {
        this.rankfrom = rankfrom;
    }

    public String getRankto() {
        return rankto;
    }

    public void setRankto(String rankto) {
        this.rankto = rankto;
    }

    public String getRankamount() {
        return rankamount;
    }

    public void setRankamount(String rankamount) {
        this.rankamount = rankamount;
    }
}
